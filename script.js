const apiKey = '5e93fc947357f44bb9d12f9dd4e6ca31';

async function getWeather() {
    const city = document.getElementById('cityInput').value.trim();
    if (city === "") {
        alert("Please enter a city name");
        return;
    }

    const weatherUrl = `https://api.openweathermap.org/data/2.5/weather?q=${encodeURIComponent(city)}&appid=${apiKey}&units=metric`;
    const forecastUrl = `https://api.openweathermap.org/data/2.5/forecast?q=${encodeURIComponent(city)}&appid=${apiKey}&units=metric`;

    try {
        const [weatherResponse, forecastResponse] = await Promise.all([
            fetch(weatherUrl),
            fetch(forecastUrl)
        ]);

        if (!weatherResponse.ok || !forecastResponse.ok) {
            throw new Error("City not found");
        }

        const weatherData = await weatherResponse.json();
        const forecastData = await forecastResponse.json();

        displayWeather(weatherData);
        displayForecast(forecastData);
    } catch (error) {
        alert(error.message);
    }
}

function displayWeather(weatherData) {
    const weatherInfo = document.getElementById('weatherInfo');
    const cityName = weatherData.name;
    const temperature = Math.round(weatherData.main.temp);
    const feelsLike = Math.round(weatherData.main.feels_like);
    const humidity = weatherData.main.humidity;
    const windSpeed =   weatherData.wind.speed;
    const weatherCondition = weatherData.weather[0].description;
    const icon = getWeatherIcon(weatherData.weather[0].main);

    document.getElementById('cityName').textContent = cityName;
    document.getElementById('temperature').textContent = temperature;
    document.getElementById('condition').textContent = weatherCondition;
    document.getElementById('feelsLike').textContent = `${feelsLike}°C`;
    document.getElementById('humidity').textContent = `${humidity}%`;
    document.getElementById('windSpeed').textContent = `${windSpeed} m/s`;
    document.getElementById('weatherIcon').innerHTML = icon;

    weatherInfo.style.display = 'block';
}

